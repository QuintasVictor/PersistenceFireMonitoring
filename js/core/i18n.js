  function cleanLang(lang) {
    return P.I18N[lang] ? lang : DEFAULT_LANG;
  }

  try {
    P.lang = cleanLang(localStorage.getItem(STORAGE_KEY) || DEFAULT_LANG);
  } catch (err) {
    P.lang = DEFAULT_LANG;
  }

  P.t = function (key) {
    var dict = P.I18N[P.lang] || P.I18N[DEFAULT_LANG];
    var fallback = P.I18N[DEFAULT_LANG] || {};
    return dict[key] != null ? dict[key] : (fallback[key] != null ? fallback[key] : key);
  };

  P.tf = function (key, vars) {
    var text = P.t(key);
    vars = vars || {};
    return text.replace(/\{([a-zA-Z0-9_]+)\}/g, function (_, name) {
      return vars[name] == null ? '' : vars[name];
    });
  };

  P.applyI18n = function (root) {
    root = root || document;
    root.querySelectorAll('[data-i18n]').forEach(function (node) {
      node.innerHTML = P.t(node.getAttribute('data-i18n'));
    });
    root.querySelectorAll('[data-i18n-text]').forEach(function (node) {
      node.textContent = P.t(node.getAttribute('data-i18n-text'));
    });
    root.querySelectorAll('[data-i18n-attr]').forEach(function (node) {
      node.getAttribute('data-i18n-attr').split(';').forEach(function (pair) {
        var parts = pair.split(':');
        if (parts.length === 2) node.setAttribute(parts[0].trim(), P.t(parts[1].trim()));
      });
    });
    document.documentElement.lang = P.lang === 'en' ? 'en' : 'pt-BR';
    document.title = P.t('app.documentTitle');
  };

  P.setLang = function (lang) {
    var next = cleanLang(lang);
    if (next === P.lang) {
      P.applyI18n(document);
      return;
    }
    P.lang = next;
    try { localStorage.setItem(STORAGE_KEY, next); } catch (err) {}
    P.applyI18n(document);
    if (P.bus && P.bus.emit) P.bus.emit('i18n:change', next);
  };
